				She was determined to blow faster than Mother Wind. 
				</p>
				<p>One day, Wendy blew her strongest, snapping twigs, and whirling 
				them through the air. She exclaimed, “See, I am greater than Mother Wind!” Blinded by envy, and not watching where 
				she was blowing, she plunged into some soft, green, moss. 
				<p>She bounced back up, frowned, brushed herself off, 
				and began blowing once again. “Nothing can keep me down for long,” boasted Wendy. 
				</p>
			</div>
			<div id="wendyfullpage">
				<p>Dodging small forest trees, 
				she laughed and said, “By tomorrow, I shall beat Mother Wind in a race. 
				<br />”Grinning, she pushed herself a little harder, 
				closed her tiny eyes, and crashed face first into a tree!
				</p>
				<p>To this day, no matter how hard Wendy blows, she remains stuck on the bark of a tree.
				</p>
				<p>Will Wendy ever get unstuck from the tree?
				</p>
				<img src="images/turtle.jpg" alt="image of turtle" id="turtleimg">
				<p>One hot, humid day Wendy cried, “I’ll never be free from this tree!” She sputtered a small gust of wind, yawned,
				and fell fast asleep. Terrance the turtle plodded by, dragging a huge stick. Dripping with sweat, he collapsed and yelled, “Help!” 
				Wendy’s eyes snapped open, she asked, “What does help mean?”
				</p>
				<p>
				“Help means when you can’t do something yourself, someone assists you in doing it. My name is Terrance, and I need
				help getting this scrumptious stick to my grandma’s house, it’s her birthday gift.”
				</p>
				<p>Wendy chuckled, “I will help you.” “You’re a tree, you can’t help me.” “Actually,  my name is Wendy, and I‘m a 
				little gust of wind stuck to a tree. It’s quite simple, grab the stick in your mouth, roll onto your shell, and get 
				ready for a speedy ride.” Terrance cheered, “I’m all set!” Wendy blew a big gust, and Terrance went sailing down the
				path on his shell squealing, “Weeeeeeeeee…..Thank you Wendy!”
				</p>
				<p>Wendy waved goodbye and thought, “Maybe if I ask Mother Wind for help she would assist me.” She yelled, “Mother Wind Help!” 
				Trees swayed, leaves whirled about, and Mother Wind appeared with a smile as wide as the ocean.
				</p>
				<p>Wendy exclaimed, “Hi Mother Wind!”
				</p>
				<p>Mother Wind replied, “Hello Wendy.”
				</p>
				<p>“You know my name.” Mother Wind nodded. “Did you know I’ve been stuck here?”
				</p>
				<p>“Yes.”
				</p>
				<p>“Why didn’t you help me?”
				Mother Wind gazed at Wendy. “Because you were determined to do it for yourself and I didn’t want to interfere. You also needed to learn 
				to ask for help. Now I can help you. Are you ready to go?”
				</p>
				<p>“I sure am.”
				Suddenly, a dark funnel cloud appeared overhead, the wind roared, the ground shook, and Mother Wind cried, “Oh no! It’s my sister 
				Tessa Tornado. She’s angry with me and chasing me. I must leave now.”
				</p>
				<p>“Mother Wind, what about me?”
				</p>
				<p>“It’s too dangerous for you to come, you’ll be safer here. Trust me, I’ll be back.” Like a flash of lightening, Mother Wind vanished.
				</p>
				<p>A tear rolled down Wendy’s cheek. “Just when I thought I’d be free, I’m still stuck to this tree.”
				  Will Mother Wind return to free Wendy? 
				
				<!--h2>July</h2-->
				<p>Tessa Tornado spun through the forest sounding like an out of control freight train. Falling trees shook the ground, 
				   and branches ceaselessly whirled overhead. Wendy felt amazingly safe and whispered, “It’s like a giant bubble is 
				   protecting me from all harm, just like Mother Wind promised.”
				</p>
				<p>Moments later, the forest grew quiet again. Wendy’s eyes darted in every direction searching for Mother Wind.
				   Weeks passed with still no sign of her return. Wendy cried out, “I hope Mother Wind is alright. If she doesn’t 
				   return to help me I’ll be stuck on this tree for eternity.”
				</p>
				<p>At sunset, a flying wild eyed creature landed with a “BOOM!” It’s dripping wet, seaweed coated hair was tangled
				in a fish net. Wendy trembled and wanted to scream but was too terrified. In a low quivering voice the creature said, 
				“Hellllloooo Wennnndy.” Wendy sheepishly smirked, and squeaked out a faint, “Hi.” 
				</p>
				<p>The creature coughed up gallons of salt water, shook itself dry, and said, “That was a close call. I almost didn’t make it back.
					Tessa Tornado picked me up and threw me into the middle of the ocean.”
				</p>
				<p>Wendy yelled, “Mother Wind it’s you! Welcome back!” Mother Wind’s face glistened like a golden sunrise.
				   “Are you ready to go Wendy?” “Yes! Blow your hardest and set me free!” With great force Mother Wind blew but 
				   Wendy didn’t move. She blew over and over again but Wendy remained stuck.
				</p>
				 <p>“What’s wrong Mother Wind?”
					Mother Wind glanced skyward, and remained quiet for quite some time. She gazed at Wendy, her blue eyes sparkling like dewdrops in 
					the sunlight. Smiling she said, “Always remember, even when someone helps you, you can do a little to help yourself too. So, let’s 
					count to three. Then together, we’ll blow our mightiest gust ever.” One, two, three……………. Whooohhhhhh…………………”
					“Weeeeeeeeeeeeeeee!” cheered Wendy. “I’m free! I’m finally free! Thank you Mother Wind!”
				</p>
				<p>	“Hop on!” laughed Mother Wind. Wendy dove onto her back.
					“Where would you like to go? We have the whole world to explore!”
				</p>
				<p>“Well let me see,” said Wendy. “How about……………….”
				</p>
				<p>Where will Wendy and Mother Wind go on their first adventure? 
				</p>
