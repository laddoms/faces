				<p>Grinning and rubbing her hands together Wendy exclaimed, “Mother Wind, on our first adventure I would 
				like to go to HA HA HA CHOOOOO!” “Bless you, Wendy. Did you mean Hawaii?” “No. I meant HA HA HA CHOOOO!”
				</p>
				<p>“Are you catching a cold Wendy?” 
				</p>
				<p>“No.” Wendy scrunched up her face and tightly pinched the tip of her nose. 
				   In a nasally voice she replied, “Excuse me, Mother Wind, but the seaweed in your hair smells like rotten fish 
				   so the first place in the world I’d like to go to is a crystal clear lake where you can wash your hair.”
				</p>
				<p>“Sorry about the terrible odor. I know the perfect place to take a dip and smell sweet again. Hold on tight, 
				   here we go!” “Weeeeeeeeeeeee!” screeched Wendy tightly grasping the fish net on Mother Wind’s hair.
				   Mother Wind blew up, down, and all around.  Wendy’s laughter filled the air.
				  “Weeeeeeeeeeeeeeee! You are the greatest, Mother Wind! I feel like we are going one million miles per hour!”
				  Pine trees wildly whirled around as Mother Wind plummeted at an incredible rate of speed towards the sparkling water.
				</p>
				<p>“Are you ready for your swim, Wendy?”
				</p>
				<p>“Yes!”  A few feet from the water’s surface Wendy saw two eyes peering up at her. She screamed, “No, Mother Wind! Stop!”
				  Mother Wind grimaced, pulling herself back up as quickly as she could. “Vvoooooooshhhh!”
				</p>
				<p>“Whoooahhhhhhh, Mother Wind! Watch out for the trees!”
				 “BOOM!” She crashed on the ground.
				</p>
				<p>“Ouch!” cried Mother Wind, coughing up dirt. Stunned, she pulled herself up and brushed herself off.
				 “Phew! Now that was a close call!” exclaimed Wendy.
				</p>
				<p>“It sure was! Next time, please give me a little more notice before you want to stop.”
				</p>
				<p>“I’m sorry. I couldn’t help it. At the last minute, I saw my friend Terrance the turtle in the water and we almost crashed right into him.”
				</p>
				<p>“Oh, now I understand. I certainly wouldn’t want to hurt your friend. Do you want to go back for a visit?”
				</p>
				<p>“Yes, I’d love to see Terrance. Let’s surprise him by quietly swimming underwater and popping up right in front of him.”
				 Did Wendy really see Terrance the turtle in the water or was some other creature lurking beneath the surface? 
				
				<!--h2>September</h2-->
				<P>Silent as falling snow, Mother Wind and Wendy gently slipped into the lake. Moments later they saw
				   Terrance’s tiny legs paddling overhead. Mother Wind popped her head up in front of him and said, “Surprise!”
				   With bulging eyes, Terrance screamed, “Ahhhhhhhhh! It’s the seaweed serpent! Help!”
				</p>
				<p>Mother Wind slid back underwater. Wendy emerged smiling and said, “Terrance, calm down. It’s me, your friend Wendy.
				   Remember me?” 
				</p>
				<p>Terrance was panting and shaking. Squinting he whispered, “Hi Wendy. Phew, am I glad to see you. Watch out, the seaweed serpent is hiding 
				  directly behind you.”
				</p>
				<p>Wendy giggled, “That’s not a serpent. It’s Mother Wind with seaweed in her hair. Thanks to you teaching me how 
				   to ask for help, I called upon Mother Wind for assistance. Together we blew a mighty gust which freed me from 
				   the tree I was stuck on. We’ve been together ever since.”
				</p>
				<p>“Oh, Wendy, that’s wonderful! Where will you go next?”
				</p>
				<p>“First, Mother Wind needs to take a bath and wash that stinky seaweed out of her hair.”
				</p>
				<p>“Great idea!” cheered Terrance. “I’ll get her.” He dove down and motioned for Mother Wind to follow him. 
				  They poked their heads up at the same time.
				</p>
				<p>Wendy smiled, “Mother Wind this is my friend Terrance.”
				</p>
				<p>“Well hello Ter Ha Ha HA!…” Wendy wrapped herself around Terrance and disappeared underwater.
				  “CHOOOOOOOOOOOOOOOOOOO!” Mother Wind’s hurricane strength sneeze sent the fish net and seaweed flying through the air. 
				  The water looked like ocean waves thunderously crashing on the shoreline. Meanwhile, Wendy and Terrance were safely 
				  swimming along the bottom of the lake.
				  When it was quiet, they emerged and were greeted by a white light. Mother Wind’s long silvery hair glistened like a 
				  million moonbeams!
				</p>
				<p>“Mother Wind!” exclaimed Wendy, “You look stunning and smell as fresh as the rain. Where did the fish net and seaweed go?”
				</p>
				<p>Mother Wind pointed towards the tree tops. “Wow!” said Terrance. “Your sneeze must have launched them up there like a rocket!”
				 The three of them laughed.
				</p>
				<p>“Let’s have some fun,” said Mother Wind. “What would you both like to do?”
				</p>
